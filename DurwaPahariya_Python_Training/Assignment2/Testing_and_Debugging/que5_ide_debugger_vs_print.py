# Advantages of Using an IDE Debugger
#
# Print statements are useful for checking
# values in small programs, but they become
# difficult to manage as the code grows.
#
# IDE debuggers provide a more structured
# and efficient way to identify and fix issues.
#
# Print Statements:
# - Require adding print() statements in code.
# - Useful for basic debugging.
# - Can make the output cluttered.
# - Need to be removed after debugging.
#
# IDE Debugger:
# - Executes code step by step.
# - Allows inspection of variables in real time.
# - Supports breakpoints to pause execution.
# - Helps locate errors faster.
# - Does not require modifying the code.
#
# Advantages of an IDE Debugger:
#
# 1. Step-by-Step Execution
#    Makes it easier to understand how
#    the program is running.
#
# 2. Variable Inspection
#    Variable values can be checked at
#    any point during execution.
#
# 3. Breakpoints
#    Execution can be paused at specific
#    lines for detailed analysis.
#
# 4. Faster Error Detection
#    Helps identify logical and runtime
#    errors more efficiently.
#
# 5. Cleaner Code
#    No need to add multiple print()
#    statements throughout the program.
#
# Example:
#
# Print Debugging:
# total = 100
# print(total)
#
# IDE Debugging:
# Set a breakpoint on the line containing
# 'total' and inspect its value directly
# while the program is running.
#
# Conclusion:
# IDE debuggers provide better visibility,
# control, and efficiency, making debugging
# easier compared to using print statements.